package Module1;

public class GettingStarted {
    public static void main(String[] args) {
        System.out.println("A well-formed Java program has a main");
        System.out.println("method with { and } braces.\n");
        System.out.println("A System.out.println statement has ( and )");
        System.out.println("and usually a String that starts and ends");
        System.out.println("with a \" character. (But we type \\\" instead!)\n");
        System.out.println("\"Comments\" are text that is to be ignored");
        System.out.println("by the compiler.  // is the symbol for a single");
        System.out.println("line comment and /* begins a block comment");
        System.out.println("while */ ends a block comment.\n");
        System.out.println("Escape sequences are special commands that");
        System.out.println("can be used inside of double quotes.  \\\"");
        System.out.println("displays a \", \\' displays a \', \\t inserts");
        System.out.println("a tab, and \\n moves the cursor to the");
        System.out.println("next line.");
    }
}